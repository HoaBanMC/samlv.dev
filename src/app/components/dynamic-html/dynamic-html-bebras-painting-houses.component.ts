export class DynamicHtmlBebrasPaintingHouses implements OnInit {
  @Output() userResponse = new EventEmitter<object>();
  @Input() content: string;
  @Input() explain: any;

  svgMain: any;
  configDiv: any;
  listColor = ["default", "red", "blue", "green"];
  userAnswer: any[] = [];

  constructor(private elementRef: ElementRef<HTMLCanvasElement>) {}

  ngOnInit() {}

  changeColor(indexParent: number) {
    if (!this.explain) {
      let currentColor = "default";
      const currentEl = this.elementRef.nativeElement.querySelectorAll(
        `#item-${indexParent}`
      )[0];
      currentEl.childNodes.forEach((item: any) => {
        if (item.id === "canPaint" && !item.classList.contains("csvg-hidden")) {
          currentEl.childNodes.forEach((item: any) => {
            if (
              item.id !== "canPaint" &&
              !item.classList.contains("csvg-hidden")
            ) {
              item.classList.add("csvg-hidden");
              currentColor = item.id;
            }
          });
        }
      });
      const nextColor = this.getNextColor(currentColor);
      currentEl.childNodes.forEach((item: any) => {
        if (item.id === nextColor) {
          item.classList.remove("csvg-hidden");
        }
      });
      this.userAnswer = this.userAnswer.map((item: any) => {
        return {
          ...item,
          value: item.index === indexParent ? nextColor : item.value,
        };
      });
      const userResponseData = this.userAnswer.map((item: any) => {
        return `${item.index}-${item.value}`;
      });
      this.userResponse.emit(userResponseData);
    }
  }

  getNextColor(currentColor: string) {
    let nextColor = "default";
    const currentIndex = this.listColor.findIndex(
      (color: string) => color === currentColor
    );
    if (currentIndex >= 0) {
      nextColor =
        currentIndex === this.listColor.length - 1
          ? this.listColor[0]
          : this.listColor[currentIndex + 1];
    }
    return nextColor;
  }

  loadComplete() {
    this.initConfig();
  }

  ok() {
    console.log(this.userAnswer);
  }

  clear() {
    if (this.userAnswer?.length) {
      this.initConfig();
      this.userAnswer = [];
    }
  }

  initConfig() {
    setTimeout(() => {
      console.log("active!");
      this.configDiv = this.elementRef.nativeElement.querySelector("#config");
      const config = JSON.parse(this.configDiv?.getAttribute("config"));
      // active canPaint
      this.svgMain = this.elementRef.nativeElement.querySelectorAll(".item");
      this.svgMain.forEach((item: any) => {
        item.childNodes.forEach((item2: any) => {
          if (
            item2.id === "canPaint" &&
            config.value[item.id.split("-")[1]] === "default"
          ) {
            item2.classList.remove("csvg-hidden");
            this.userAnswer.push({
              index: +item.id.split("-")[1],
              value: "default",
            });
          } else {
            if (item2.id === "default") {
              item2.classList.add("csvg-hidden");
            }
            if (item2.id === config.value[item.id.split("-")[1]]) {
              item2.classList.remove("csvg-hidden");
            }
          }
        });
      });
      this.userAnswer = this.userAnswer.sort((a, b) => a.index - b.index);
      if (!this.explain) {
        const userResponseData = this.userAnswer.map((item: any) => {
          return `${item.index}-${item.value}`;
        });
        this.userResponse.emit(userResponseData);
      }
    }, 500);
  }
}
